package rf.mizuka.web.application.models.tracks;

import jakarta.persistence.*;

/**
 * ORM‑сущность JPA, представляющая аудиотрек (аудиофайл) в реляционной базе данных.
 *
 * <p>Класс отображается на таблицу {@code tracks} и используется как Java‑объект‑домена,
 * который взаимодействует с базой данных через механизм JPA и Hibernate (как провайдер JPA
 * в Spring Boot по умолчанию). В Spring‑контексте такие сущности обслуживаются
 * уровнями репозиториев (например, {@code JpaRepository<Track, Long>},
 * настройка которых опирается на Spring Data JPA и автоматическую конфигурацию
 * {@code spring-boot-starter-data-jpa}).</p>
 *
 * <p>Механизм работы в контексте Spring и JPA:
 * <ul>
 *   <li>Класс помечен аннотацией {@link jakarta.persistence.Entity @Entity},
 *   что указывает JPA‑провайдеру (например Hibernate) рассматривать его как сущность,
 *   отображаемую на таблицу базы данных. JPA‑контейнер управляет жизненным циклом
 *   объекта: создание, сохранение, обновление, удаление и привязка к базе через
 *   {@link jakarta.persistence.EntityManager} или, в Spring, через Spring Data JPA‑репозитории.</li>
 *   <li>Аннотация {@link jakarta.persistence.Table @Table(name = "tracks")} задаёт
 *   целевое имя таблицы в базе данных, куда будет маппиться данная сущность.
 *   Это позволяет явно контролировать схему, префикс и другие свойства таблицы,
 *   даже если в DAO/репозитории используются стандартные JPA‑операции.</li>
 *   <li>Поле {@code id} помечено аннотацией {@link jakarta.persistence.Id @Id},
 *   указывая, что это первичный ключ таблицы {@code tracks}. В сочетании с
 *   {@link jakarta.persistence.GeneratedValue @GeneratedValue(strategy = GenerationType.IDENTITY)}
 *   это означает, что значение идентификатора генерируется базой данных (например
 *   через автоинкрементный столбец), а JPA‑провайдер после {@code INSERT} автоматически
 *   обновляет значение {@code id} в объекте. Это стандартный подход для генерации
 *   суррогатных ключей и обеспечения их уникальности и целостности.</li>
 *   <li>Поле {@code name} отображается на столбец с помощью
 *   {@link jakarta.persistence.Column @Column(nullable = false)}. Это указывает,
 *   что в таблице будет создан NOT NULL‑столбец, не допускающий NULL‑значений.
 *   Такая схема гарантирует базовую инвариантность данных: у каждого трека
 *   должно быть название, иначе вставка в БД будет отклонена на уровне DDL/Data Integrity.
 *   Это также повышает согласованность модели: имя трека всегда определено в сохранённых записях.</li>
 *   <li>Класс содержит стандартный безаргументный конструктор, необходимый JPA для
 *   рефлексивного создания экземпляров сущностей при загрузке данных из базы
 *   (например, через {@code findById}, {@code findAll} и другие методы репозитория).
 *   JPA‑провайдеры требуют доступный конструктор по умолчанию; без него объекты
 *   не могли бы быть созданы при работе с ORM‑маппингом.</li>
 *   <li>Аксессоры и сеттеры следуют стандартному бин‑паттерну, который используется
 *   JPA и Spring Data JPA для привязки и изменения состояния объекта. Особенностью
 *   данного класса является возврат самого объекта из сеттеров (fluent API),
 *   что позволяет удобно строить объекты при ручной инициализации, несмотря на то,
 *   что это не влияет на поведение JPA напрямую.</li>
 * </ul>
 * </p>
 *
 * <p>Интеграция с Spring‑приложением:
 * <ul>
 *   <li>Сущность становится частью доменной модели, обрабатываемой Spring Data JPA.
 *   При наличии интерфейса вроде {@code TrackRepository extends JpaRepository<Track, Long>}
 *   Spring автоматически создаёт реализацию репозитория, покрывающую стандартные CRUD‑операции
 *   и позволяющую выполнять кастомные запросы через методы репозитория.</li>
 *   <li>Spring Boot, через auto‑configuration для JPA и Hibernate, настраивает
 *   соединения с базой данных, провайдер, DataSource и транзакции на основе
 *   конфигурации (application.properties / application.yml и зависимостей типа
 *   spring-boot-starter-data-jpa). Это позволяет использовать объекты {@code Track}
 *   в сервисах и контроллерах без ручной инициализации EntityManager и JPA‑инфраструктуры.</li>
 *   <li>В сервисах работа с {@code Track} обычно обёрнута транзакциями (через
 *   {@link org.springframework.transaction.annotation.Transactional}), обеспечивая
 *   атомарность операций сохранения, обновления или удаления. Внутри транзакции
 *   JPA‑провайдер управляет кэшем первого уровня ( persistence context),
 *   отслеживая изменения объектов и генерируя DML‑запросы при необходимости.</li>
 *   <li>При возврате экземпляров {@code Track} из контроллеров (например, REST‑контроллеров)
 *   Spring автоматически сериализует их в JSON/XML через стандартные конвертеры
 *   (например, Jackson), если на уровне репозитория или сервиса объект уже загружен
 *   из БД и находится в управлении persistence context. Это позволяет легко
 *   представлять доменные сущности в виде REST‑ресурсов без дополнительных DTO,
 *   если бизнес‑логика и структура данных этого не препятствуют.</li>
 * </ul>
 * </p>
 *
 * <p>Таким образом, данный класс:
 * <ul>
 *   <li>является JPA‑сущностью, отображённой на таблицу {@code tracks};</li>
 *   <li>содержит первичный ключ {@code id} и обязательное поле {@code name};</li>
 *   <li>поддерживает стандартный JPA‑маппинг и совместимость с Spring Data JPA;</li>
 *   <li>готов к использованию в Spring Boot‑приложении с автоматической конфигурацией
 *   ORM, DataSource и транзакций.</li>
 * </ul>
 * </p>
 */
@Entity
@Table(name = "tracks")
public class Track {
    /**
     * Уникальный идентификатор трека — первичный ключ в таблице {@code tracks}.
     *
     * <p>Поле помечено аннотацией {@link jakarta.persistence.Id @Id}, что указывает JPA
     * на то, что это поле представляет собой первичный ключ сущности {@code Track}.
     * В базе данных соответствующий столбец будет использоваться для уникальной идентификации
     * каждой записи, а также для построения связей (внешних ключей) из других таблиц.</p>
     *
     * <p>Аннотация {@link jakarta.persistence.GeneratedValue @GeneratedValue(strategy = GenerationType.IDENTITY)}
     * означает, что значение идентификатора будет генерироваться базой данных (например, через
     * автоинкрементное поле {@code AUTO_INCREMENT} в MySQL или {@code SERIAL} в PostgreSQL).
     * Это стандартный подход для суррогатных ключей: при вставке новой записи база данных
     * сама назначает следующее свободное значение, а JPA‑провайдер (например Hibernate)
     * после выполнения {@code INSERT} забирает это значение и обновляет поле {@code id}
     * в объекте в памяти.</p>
     *
     * <p>Такая схема:
     * <ul>
     *   <li>гарантирует уникальность и монотонность значений {@code id} для вновь созданных треков;</li>
     *   <li>упрощает работу с репозиториями Spring Data JPA, так как методы вроде
     *   {@code findById(Long id)}, {@code deleteById(Long id)} и {@code existsById(Long id)}
     *   напрямую используют тип {@code Long} как ключ;</li>
     *   <li>сохраняет идентификатор read‑only для уровня приложения до момента сохранения:
     *   до вызова {@code save} значение может быть {@code null}, что явно отражает
     *   состояние «объект ещё не сохранён в БД», а после сохранения в БД — реальным значением,
     *   присвоенным базой данных.</li>
     *   <li>использование {@code Long} (обёртка) вместо примитивного {@code long} позволяет
     *   корректно представлять отсутствие идентификатора в модели объекта до сохранения,
     *   что совместимо с семантикой JPA и REST‑сервисов, где {@code null} в JSON
     *   означает «новый объект, ещё не persisted», а не условное значение {@code 0}.</li>
     * </ul>
     * </p>
     */
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;
    /**
     * Название трека — обязательное поле в таблице {@code tracks}.
     *
     * <p>Поле помечено аннотацией {@link jakarta.persistence.Column @Column(nullable = false)},
     * что указывает JPA‑провайдеру (например Hibernate) и базе данных:
     * <ul>
     *   <li>создать в таблице {@code tracks} столбец {@code name} с ограничением NOT NULL,
     *   не допускающим вставку записи с пустым значением имени;</li>
     *   <li>гарантировать базовую инвариантность данных: у каждого трека всегда должно быть название,
     *   иначе операция вставки будет отклонена на уровне СУБД или валидатора.</li>
     * </ul>
     * </p>
     *
     * <p>Семантический смысл использования {@code String name}:
     * <ul>
     *   <li>поле хранит текстовое имя трека, используемое в UI‑слое (HTML‑страницы, API‑ответы)
     *   и в бизнес‑логике (поиск, фильтрация, отображение в списке треков);</li>
     *   <li>грязные или неполные данные ({@code null} или пустая строка) отсекаются
     *   не только на уровне валидаторов Java‑кода, но и на уровне схемы БД,
     *   что обеспечивает единый контракт целостности данных;</li>
     *   <li>ограничение {@code nullable = false} совместимо с типичным поведением
     *   Spring Data JPA‑репозиториев и валидаторов (например {@code @NotBlank}),
     *   которые дополнительно проверяют корректность значения до сохранения в БД.</li>
     * </ul>
     * </p>
     */
    @Column(nullable = false)
    private String name;
    /**
     * Умолчательный (безаргументный) конструктор, необходимый JPA для создания экземпляров сущности.
     *
     * <p>JPA‑провайдер (например Hibernate) требует наличия доступного безаргументного конструктора
     * для рефлексивного создания объектов {@code Track} при загрузке данных из базы данных
     * через {@link jakarta.persistence.EntityManager} или методы Spring Data JPA‑репозиториев
     * ({@code findById}, {@code findAll}, {@code save} и т.п.).</p>
     *
     * <p>Без этого конструктора JPA не сможет корректно инстанцировать объекты
     * при работе с ORM‑маппингом, даже если в классе есть другие конструкторы.
     * Стандартное поведение:
     * <ul>
     *   <li>поля вновь созданного объекта инициализируются значениями по умолчанию
     *   (например {@code id = 0L} / {@code null} для примитивов и объектов);</li>
     *   <li>после создания JPA‑провайдер сам инициализирует поля (например {@code id})
     *   на основе данных, полученных из базы, либо оставляет их как есть, если
     *   объект вручную создаётся приложением и ещё не сохранён.</li>
     * </ul>
     * </p>
     */
    public Track() {}
    /**
     * Возвращает название трека.
     *
     * <p>На уровне JPA поле {@code name} отображается на столбец {@code name} таблицы {@code tracks}
     * с ограничением {@code NOT NULL} (через {@link jakarta.persistence.Column @Column(nullable = false)}).
     * Это означает, что база данных не примет запись с {@code null} в этом поле,
     * и JPA‑провайдер при сохранении сущности ожидает корректное значение (не null).
     * Тип {@code String} позволяет хранить произвольное текстовое имя трека
     * в рамках ограничений, заданных в БД (например, длина строки).</p>
     *
     * <p>Название трека используется в:
     * <ul>
     *   <li>UI‑слое (HTML‑страницы, JSON‑ответы REST‑контроллера) для отображения
     *   имени трека конечному пользователю;</li>
     *   <li>логике приложения (поиск, фильтрация, агрегация) при работе с доменной моделью,
     *   где важно идентифицировать трек по текстовому имени.</li>
     * </ul>
     * </p>
     *
     * @return название трека; не может быть {@code null} для уже сохранённых записей
     *         в базе данных, но может быть {@code null} или пустой строкой для объектов,
     *         созданных вручную до сохранения.
     */
    public String getName() {
        return name;
    }
    /**
     * Устанавливает название трека.
     *
     * <p>На уровне JPA этот метод изменяет поле {@code name}, которое отображается
     * на столбец {@code name} таблицы {@code tracks}. При последующем вызове
     * метода сохранения через JPA‑провайдер или Spring Data JPA репозиторий
     * новое значение будет записано в базу данных (если объект в состоянии
     * "managed" и изменение отслеживается в persistence context).</p>
     *
     * <p>Ограничения и семантика:
     * <ul>
     *   <li>поскольку в БД столбец помечен как {@code NOT NULL},
     *   валидация на уровне приложения (например через {@code @NotBlank})
     *   должна гарантировать, что перед сохранением в БД значение не является
     *   {@code null} или пустой строкой, чтобы не нарушить контракт целостности данных;</li>
     *   <li>сеттер возвращает {@code this}, предоставляя fluent API для удобного
     *   построения объекта при ручной инициализации, хотя на поведение JPA это не влияет.</li>
     * </ul>
     * </p>
     *
     * @param name новое название трека; может быть {@code null} или пустой строкой
     *             при создании объекта вручную, но перед сохранением в БД должно быть
     *             корректным и не {@code null} значением, если используется
     *             валидация или ограничения в базе данных.
     * @return текущий объект {@code Track}, чтобы можно было использовать fluent API
     *         при необходимости (например {@code new Track().setName("...").save(...)}).
     */
    public Track setName(String name) {
        this.name = name;
        return this;
    }
    /**
     * Возвращает уникальный идентификатор трека.
     *
     * <p>На уровне JPA поле {@code id} отображается на первичный ключ таблицы {@code tracks}
     * с аннотациями {@link jakarta.persistence.Id @Id} и
     * {@link jakarta.persistence.GeneratedValue @GeneratedValue(strategy = GenerationType.IDENTITY)}.
     * Это означает, что значение идентификатора генерируется базой данных автоматически
     * (через автоинкремент), а JPA‑провайдер после {@code INSERT} обновляет поле в объекте.
     * До сохранения в БД значение может быть {@code null}, что семантически отражает
     * состояние «новый объект, ещё не persisted».</p>
     *
     * <p>Идентификатор используется в:
     * <ul>
     *   <li>методах Spring Data JPA‑репозиториев ({@code findById(Long id)},
     *   {@code deleteById(Long id)}, {@code existsById(Long id)}) для точной идентификации
     *   конкретного трека;</li>
     *   <li>REST‑API (JSON‑ответы, пути URL типа {@code /tracks/{id}});</li>
     *   <li>связях с другими сущностями (внешние ключи в таблицах playlist_tracks,
     *   user_favorites и т.п.), где {@code track_id} ссылается на этот {@code id}.</li>
     * </ul>
     * </p>
     *
     * <p>Использование типа {@code Long} (обёртка) вместо {@code long} позволяет корректно
     * представлять {@code null} для несохранённых объектов в JSON и бизнес‑логике,
     * избегая ложных значений типа {@code 0L}, которые могли бы интерпретироваться
     * как валидный идентификатор.</p>
     *
     * @return уникальный идентификатор трека; {@code null} для объектов, созданных вручную
     *         и ещё не сохранённых в базе данных, реальное значение {@code Long} — для
     *         уже persisted сущностей, загруженных из БД или после успешного {@code save}.
     */
    public Long getId() {
        return id;
    }

    public Track setId(Long id) {
        this.id = id;
        return this;
    }
}