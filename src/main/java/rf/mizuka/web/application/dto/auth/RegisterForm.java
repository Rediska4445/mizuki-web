package rf.mizuka.web.application.dto.auth;

/**
 * DTO‑класс для передачи данных с формы регистрации (логин / пароль / подтверждение пароля)
 * между HTML‑страницей регистрации и компонентами регистрации/аутентификации серверной части.
 *
 * <p>Используется для:
 * <ul>
 *   <li>сбора данных, отправленных клиентом через форму регистрации (например, из Thymeleaf‑формы
 *   или обычной HTML‑form с методом POST);</li>
 *   <li>передачи этих данных в контроллеры, сервисы регистрации или валидаторы,
 *   которые проверяют корректность введённых данных и создают нового пользователя.</li>
 * </ul>
 * </p>
 *
 * <p>Класс не содержит:
 * <ul>
 *   <li>логики валидации (проверка совпадения пароля и его подтверждения, сложности пароля и т.п.
 *   может быть вынесена в отдельные слои или аннотации, например @Valid, @Constraint);</li>
 *   <li>логики хранения или шифрования пароля (пароль и его подтверждение в виде строки передаются
 *   так, как получены с клиента; реальное хеширование и сохранение остаются
 *   ответственностью сервиса регистрации и аутентификации).</li>
 * </ul>
 * </p>
 *
 * <p>Структура и принципы:
 * <ul>
 *   <li>DTO содержит только поля и стандартные геттеры/сеттеры, что делает его простым и понятным
 *   контрактом обмена данных между слоем представления и слоем регистрации;</li>
 *   <li>методы сеттеров возвращают {@code this}, предоставляя fluent API для удобного
 *   построения объекта вручную при необходимости
 *   (например {@code new RegisterForm().setUsername("...").setPassword("...").setConfirmPassword("...")}).
 * </li>
 * </ul>
 * </p>
 */
public class RegisterForm
{
    /**
     * Имя пользователя (логин), переданное с формы регистрации или инициализированное в коде.
     *
     * <p>Поле используется как часть контракта DTO для передачи данных регистрации
     * между слоем представления (HTML‑форма) и слоем бизнес‑логики регистрации.</p>
     *
     * <p>Значение может быть {@code null} или пустой строкой, если форма не заполнена
     * или поле было явно установлено в такое состояние. На уровне этого класса
     * не выполняется валидация и проверка существования пользователя;
     * логика проверки имени пользователя (уникальность, допустимые символы и т.п.)
     * остаётся на стороне сервиса регистрации или валидаторов.</p>
     */
    private String username;

    /**
     * Пароль, переданный с формы регистрации или инициализированный в коде.
     *
     * <p>Поле хранит пароль в виде строки без дополнительного шифрования или хеширования.
     * Это означает, что ответственность за безопасную обработку пароля (например, хеширование
     * с помощью BCrypt, Argon2 и т.д.) лежит не на этом DTO, а на слое регистрации.</p>
     *
     * <p>Значение может быть {@code null} или пустой строкой, если форма не заполнена
     * или поле было явно установлено в такое состояние. Пароль должен обрабатываться
     * как конфиденциальная информация: не логироваться в чистом виде, не сохраняться
     * в открытом виде в базе данных и т.п.</p>
     */
    private String password;
    /**
     * Подтверждение пароля, переданное с формы регистрации или инициализированное в коде.
     *
     * <p>Поле используется для проверки совпадения пароля и его подтверждения
     * на уровне сервиса регистрации или валидаторов. На уровне DTO не производится
     * автоматическая проверка совпадения; логика сравнения остаётся вне этого класса.</p>
     *
     * <p>Значение может быть {@code null} или пустой строкой, если форма не заполнена
     * или поле было явно установлено в такое состояние. Как и {@code password},
     * это поле не шифруется и не обрабатывается на уровне DTO.</p>
     */
    private String confirmPassword;

    /**
     * Умолчательный (безаргументный) конструктор DTO.
     *
     * <p>Позволяет создавать пустой объект {@code RegisterForm} через рефлексию
     * (например, при использовании Spring MVC для привязки данных формы
     * или при десериализации JSON), когда не нужна немедленная инициализация полей.</p>
     *
     * <p>После вызова конструктора поля {@code username}, {@code password}
     * и {@code confirmPassword} инициализируются значениями по умолчанию (null),
     * а заполнение происходит через сеттеры или привязку данных (data binding).</p>
     */
    public RegisterForm() {}

    /**
     * Конструктор, инициализирующий логин и пароль, при этом подтверждение пароля
     * автоматически устанавливается в то же значение, что и основной пароль.
     *
     * <p>Удобен, когда клиент передаёт только логин и пароль, а подтверждение
     * пароля в контексте регистрации совпадает с основным паролем
     * (например, при использовании одной и той же строки в форме).</p>
     *
     * <p>На уровне DTO не выполняется проверка совпадения пароля и его подтверждения;
     * логика сравнения остаётся за валидатором или сервисом регистрации.</p>
     *
     * @param username имя пользователя (логин), переданное клиентом или инициализированное в коде;
     *                 может быть {@code null}.
     * @param password пароль, переданный клиентом или инициализированный в коде;
     *                 может быть {@code null}.
     */
    public RegisterForm(String username, String password) {
        this.username = username;
        this.password = password;
        this.confirmPassword = password;
    }

    /**
     * Конструктор, инициализирующий все три поля: логин, пароль и подтверждение пароля.
     *
     * <p>Позволяет создать объект {@code RegisterForm} сразу с заполненными полями,
     * что удобно при ручной инициализации или при передаче данных между слоями,
     * где уже известны все значения.</p>
     *
     * <p>Значения пароля и подтверждения могут отличаться; на уровне DTO это допустимо,
     * проверка совпадения остаётся за валидатором или сервисом регистрации.</p>
     *
     * @param username имя пользователя (логин), переданное клиентом или инициализированное в коде;
     *                 может быть {@code null}.
     * @param password пароль, переданный клиентом или инициализированный в коде;
     *                 может быть {@code null}.
     * @param confirmPassword строка подтверждения пароля, переданная клиентом или инициализированная в коде;
     *                        может быть {@code null}.
     */
    public RegisterForm(String username, String password, String confirmPassword) {
        this.username = username;
        this.password = password;
        this.confirmPassword = confirmPassword;
    }

    /**
     * Возвращает имя пользователя, переданное с формы регистрации.
     *
     * <p>Значение обычно соответствует полю ввода {@code username} или {@code login}
     * на HTML‑странице регистрации.</p>
     *
     * @return имя пользователя (логин), переданное клиентом;
     *         может быть {@code null} или пустой строкой, если форма не заполнена.
     */
    public String getUsername() {
        return username;
    }

    /**
     * Устанавливает имя пользователя (логин) для этого DTO.
     *
     * <p>Вызывается Spring MVC при привязке данных формы (data binding)
     * или при ручной инициализации объекта. Поле используется для дальнейшей
     * проверки существования пользователя и последующей регистрации.</p>
     *
     * @param username имя пользователя, переданное клиентом;
     *                 может быть {@code null} или пустой строкой.
     * @return текущий объект {@code RegisterForm}, чтобы можно было использовать fluent API.
     */
    public RegisterForm setUsername(String username) {
        this.username = username;
        return this;
    }

    /**
     * Возвращает пароль, переданный с формы регистрации.
     *
     * <p>Значение соответствует полю ввода {@code password} на HTML‑странице регистрации;
     * пароль передаётся как строка без дополнительного шифрования на уровне этого класса.</p>
     *
     * @return пароль, переданный клиентом;
     *         может быть {@code null} или пустой строкой, если форма не заполнена.
     */
    public String getPassword() {
        return password;
    }

    /**
     * Устанавливает пароль для этого DTO.
     *
     * <p>Вызывается Spring MVC при привязке данных формы (data binding)
     * или при ручной инициализации объекта. На уровне этого класса не производится
     * ни хеширование, ни какие‑либо операции с паролем; его безопасная обработка
     * остаётся ответственностью слоя регистрации.</p>
     *
     * @param password пароль, переданный клиентом;
     *                 может быть {@code null} или пустой строкой.
     * @return текущий объект {@code RegisterForm}, чтобы можно было использовать fluent API.
     */
    public RegisterForm setPassword(String password) {
        this.password = password;
        return this;
    }

    /**
     * Возвращает подтверждение пароля, переданное с формы регистрации.
     *
     * <p>Значение соответствует полю ввода {@code confirmPassword} или аналогичному
     * на HTML‑странице регистрации. На уровне DTO не проверяется совпадение
     * с основным паролем; это делается в сервисе регистрации или валидаторе.</p>
     *
     * @return подтверждение пароля, переданное клиентом;
     *         может быть {@code null} или пустой строкой, если форма не заполнена.
     */
    public String getConfirmPassword() {
        return confirmPassword;
    }

    /**
     * Устанавливает подтверждение пароля для этого DTO.
     *
     * <p>Вызывается Spring MVC при привязке данных формы (data binding)
     * или при ручной инициализации объекта. На уровне DTO не выполняется автоматическая
     * проверка совпадения пароля и его подтверждения; соответствующая логика
     * остаётся на стороне валидатора или сервиса регистрации.</p>
     *
     * @param confirmPassword строка подтверждения пароля, переданная клиентом;
     *                        может быть {@code null} или пустой строкой.
     * @return текущий объект {@code RegisterForm}, чтобы можно было использовать fluent API.
     */
    public RegisterForm setConfirmPassword(String confirmPassword) {
        this.confirmPassword = confirmPassword;
        return this;
    }
}