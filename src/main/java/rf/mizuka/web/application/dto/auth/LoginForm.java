package rf.mizuka.web.application.dto.auth;

/**
 * DTO‑класс для передачи данных с формы логина (логин / пароль)
 * между HTML‑страницей авторизации и компонентами аутентификации серверной части.
 *
 * <p>Используется для:
 * <ul>
 *   <li>сбора данных, отправленных клиентом через форму логина (например, из Thymeleaf‑формы
 *   или обычной HTML‑form с методом POST);</li>
 *   <li>передачи этих данных в контроллеры, сервисы аутентификации или классы,
 *   взаимодействующие с Spring Security (например, UserDetailsService или AuthenticationProvider).</li>
 * </ul>
 * </p>
 *
 * <p>Класс не содержит:
 * <ul>
 *   <li>логики валидации (она может быть вынесена в отдельные слои или аннотации, например @Valid);</li>
 *   <li>логики хранения или шифрования пароля (пароль в виде строки передаётся так, как получен
 *   с клиента; реальное хеширование и проверка остаются ответственностью сервиса аутентификации).</li>
 * </ul>
 * </p>
 *
 * <p>Структура и принципы:
 * <ul>
 *   <li>DTO содержит только поля и стандартные геттеры/сеттеры, что делает его простым и понятным
 *   контрактом обмена данных между слоем представления и слоем авторизации;</li>
 *   <li>использование {@code protected}‑полей означает, что DTO доступен только внутри пакета
 *   и наследникам, что ограничивает прямое изменение состояния извне;
 *   все изменения должны идти через методы доступа;</li>
 *   <li>сеттеры возвращают {@code this}, предоставляя fluent API для удобного построения объекта
 *   вручную при необходимости (например {@code new LoginForm().setUsername("...").setPassword("...")}).
 * </li>
 * </ul>
 * </p>
 */
public class LoginForm {
    /**
     * Имя пользователя (логин), переданное с формы логина или инициализированное в коде.
     *
     * <p>Поле используется как часть контракта DTO для передачи данных аутентификации
     * между слоем представления (HTML‑форма) и слоем бизнес‑логики/аутентификации.</p>
     *
     * <p>Значение может быть {@code null} или пустой строкой, если форма не заполнена
     * или поле было явно установлено в такое состояние. На уровне этого класса
     * не выполняется валидация и проверка существования пользователя;
     * логика проверки остаётся на стороне сервиса аутентификации или валидаторов.</p>
     */
    private String username;

    /**
     * Пароль, переданный с формы логина или инициализированный в коде.
     *
     * <p>Поле хранит пароль в виде строки без дополнительного шифрования или хеширования.
     * Это означает, что ответственность за безопасную обработку пароля (например, хеширование
     * с помощью BCrypt, Argon2 и т.д.) лежит не на этом DTO, а на слое аутентификации.</p>
     *
     * <p>Значение может быть {@code null} или пустой строкой, если форма не заполнена
     * или поле было явно установлено в такое состояние. Пароль должен обрабатываться
     * как конфиденциальная информация: не логироваться в чистом виде, не сохраняться
     * в открытом виде в базе данных и т.п.</p>
     */
    private String password;

    /**
     * Умолчательный (безаргументный) конструктор DTO.
     *
     * <p>Позволяет создавать пустой объект {@code LoginForm} через рефлексию
     * (например, при использовании Spring MVC для биндинга формы или Jackson
     * для десериализации JSON), когда не нужна немедленная инициализация полей.</p>
     *
     * <p>После вызова конструктора поля {@code username} и {@code password}
     * инициализируются значениями по умолчанию (null), а заполнение происходит
     * через сеттеры или привязку данных (data binding).</p>
     */
    public LoginForm() {}
    /**
     * Конструктор, инициализирующий логин и пароль.
     *
     * <p>Позволяет создать объект {@code LoginForm} сразу с заполненными полями,
     * что удобно при ручной инициализации или при передаче данных между слоями,
     * где уже известны значения логина и пароля.</p>
     *
     * <p>Поле {@code username} устанавливается из параметра {@code username},
     * поле {@code password} — из параметра {@code password}. Это не влияет
     * на логику хранения или шифрования пароля; он остаётся в виде строки
     * до уровня сервиса аутентификации.</p>
     *
     * @param username имя пользователя (логин), переданное клиентом или инициализированное
     *                 в коде; может быть {@code null}.
     * @param password пароль, переданный клиентом или инициализированное в коде;
     *                 может быть {@code null}.
     */
    public LoginForm(String username, String password) {
        this.username = username;
        this.password = password;
    }
    /**
     * Конструктор, инициализирующий только имя пользователя.
     *
     * <p>Позволяет создать объект {@code LoginForm}, в котором известен только логин,
     * а пароль будет установлен позже (например, через сеттер или при привязке данных).</p>
     *
     * <p>Используется, когда в контексте требуется только имя пользователя
     * (например, для предварительной проверки существования пользователя
     * или для восстановления состояния формы), а пароль добавляется отдельно.</p>
     *
     * @param username имя пользователя (логин), переданное клиентом или инициализированное
     *                 в коде; может быть {@code null}.
     */
    public LoginForm(String username) {
        this.username = username;
    }

    /**
     * Возвращает имя пользователя, переданное с формы логина.
     *
     * <p>Значение обычно соответствует полю ввода {@code username} или {@code login}
     * на HTML‑странице авторизации.</p>
     *
     * @return имя пользователя (логин), переданное клиентом;
     *         может быть {@code null} или пустой строкой, если форма не заполнена.
     */
    public String getUsername() {
        return username;
    }

    /**
     * Устанавливает имя пользователя (логин) для этого DTO.
     *
     * <p>Вызывается Spring MVC при привязке данных формы (data binding)
     * или при ручной инициализации объекта. Поле используется для дальнейшей
     * проверки существования пользователя и последующей аутентификации.</p>
     *
     * @param username имя пользователя, переданное клиентом;
     *                 может быть {@code null} или пустой строкой.
     * @return текущий объект {@code LoginForm}, чтобы можно было использовать fluent API.
     */
    public LoginForm setUsername(String username) {
        this.username = username;
        return this;
    }

    /**
     * Возвращает пароль, переданный с формы логина.
     *
     * <p>Значение соответствует полю ввода {@code password} на HTML‑странице;
     * пароль передаётся как строка без дополнительного шифрования на уровне этого класса.</p>
     *
     * @return пароль, переданный клиентом;
     *         может быть {@code null} или пустой строкой, если форма не заполнена.
     */
    public String getPassword() {
        return password;
    }

    /**
     * Устанавливает пароль для этого DTO.
     *
     * <p>Вызывается Spring MVC при привязке данных формы (data binding)
     * или при ручной инициализации объекта. На уровне этого класса не производится
     * ни хеширование, ни какие‑либо операции с паролем; его безопасная обработка
     * остаётся ответственностью слоя аутентификации.</p>
     *
     * @param password пароль, переданный клиентом;
     *                 может быть {@code null} или пустой строкой.
     * @return текущий объект {@code LoginForm}, чтобы можно было использовать fluent API.
     */
    public LoginForm setPassword(String password) {
        this.password = password;
        return this;
    }
}