package rf.mizuka.web.application.controllers.home;

import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestMapping;

/**
 * Контроллер главной страницы приложения Mizuka.
 *
 * <p>Этот класс отвечает за обработку HTTP‑запросов к корневому пути приложения
 * (например, GET /). Он реализует поведение Spring MVC‑контроллера:
 * принимает запросы, подготавливает модель данных и возвращает имя представления (view),
 * которое будет использовано шаблонизатором (например Thymeleaf, Freemarker или JSP) для
 * генерации HTML‑ответа.</p>
 *
 * <p>Работа класса в контексте Spring:
 * <ul>
 *   <li>Аннотация {@link org.springframework.stereotype.Controller @Controller}
 *   помечает класс как компонент‑контроллер, который обрабатывает HTTP‑запросы.
 *   Он автоматически регистрируется в Spring‑контексте как бин и становится доступен для
 *   обработки запросов через Spring MVC (DispatcherServlet).</li>
 *   <li>Аннотация {@link org.springframework.web.bind.annotation.RequestMapping @RequestMapping}
 *   задаёт базовый путь для всех методов контроллера. В данном случае указано
 *   {@code @RequestMapping("/")}, то есть все обработчики запросов этого класса будут
 *   реагировать на пути относительно корня приложения.</li>
 *   <li>Когда приходит HTTP‑запрос, Spring MVC находит подходящий контроллер по пути
 *   и выбирает метод, аннотированный соответствующей аннотацией (например {@code @GetMapping}),
 *   который совпадает с типом запроса и путём.</li>
 *   <li>Контроллер передаёт данные в шаблон через объект {@link org.springframework.ui.Model Model},
 *   который является частью контекста MVC‑запроса; Spring автоматически связывает его
 *   с параметром метода и передаёт модель в ViewResolver.</li>
 *   <li>Возвращаемое методами значение интерпретируется как имя представления (view name),
 *   а не как JSON или тело ответа; это поведение отличается от {@code @RestApiController},
 *   который по умолчанию возвращает данные напрямую клиенту.</li>
 * </ul>
 * </p>
 *
 * <p>Таким образом, класс {@code HomeController}:
 * <ul>
 *   <li>является точкой входа для HTTP‑запросов к корневому пути приложения;</li>
 *   <li>использует Spring MVC‑механизмы маппинга запросов, обработки параметров и
 *   передачи модели в шаблон;</li>
 *   <li>поддерживает стандартный MVC‑паттерн Spring Boot: контроллер → модель → представление.</li>
 * </ul>
 * </p>
 */
@Controller
@RequestMapping("/")
public class HomeController {
    /**
     * Обрабатывает HTTP GET‑запрос к корневому пути {@code /}.
     *
     * <p>Метод вызывается, когда приходит GET‑запрос на базовый путь контроллера,
     * который задан аннотацией {@code @RequestMapping("/")}, то есть на адрес:
     * <ul>
     *   <li>{@code GET /} (корень приложения).</li>
     * </ul>
     * </p>
     *
     * <p>Аннотация {@link org.springframework.web.bind.annotation.GetMapping @GetMapping}
     * является сокращением для:</p>
     * <pre>
     * @RequestMapping(method = RequestMethod.GET)
     * </pre>
     * <p>То есть метод принимает только HTTP GET‑запросы и автоматически связывается с
     * путём, указанным на уровне класса {@code @RequestMapping("/")}, образуя финальный
     * путь {@code GET /}.</p>
     *
     * <p>Параметр {@link org.springframework.ui.Model}:
     * <ul>
     *   <li>представляет контейнер данных (модель), который передаётся в шаблон (view);
     *   Spring автоматически связывает параметр с текущим MVC‑контекстом запроса;</li>
     *   <li>внутри метода можно добавлять атрибуты через {@code model.addAttribute("key", value)},
     *   которые будут доступны в шаблоне (например в Thymeleaf или Freemarker);</li>
     *   <li>в данном методе модель используется без добавления дополнительных атрибутов,
     *   т.е. в шаблон передаётся только “пустая” или предварительно подготовленная модель.</li>
     * </ul>
     * </p>
     *
     * <p>Возвращаемое значение:
     * <ul>
     *   <li>строка {@code "home"} интерпретируется не как строка для прямого ответа,
     *   а как имя представления (view name) в механизме Spring MVC;</li>
     *   <li>возвращаемое имя шаблона передаётся в {@link org.springframework.web.servlet.ViewResolver ViewResolver},
     *   который находит соответствующий шаблон (например {@code home.html} или {@code home.ftl}
     *   в настроенной директории шаблонов) и отвечает клиенту рендеренным HTML‑контентом.</li>
     * </ul>
     * </p>
     *
     * <p>Таким образом, метод:
     * <ul>
     *   <li>перехватывает HTTP GET‑запрос на корневой путь приложения;</li>
     *   <li>подготавливает модель данных (в текущей реализации без дополнительных атрибутов);</li>
     *   <li>возвращает имя представления {@code "home"}, которое используется Spring MVC
     *   для отображения HTML‑страницы главного экрана.</li>
     * </ul>
     * </p>
     *
     * @param model контейнер данных (модель), передаваемый в шаблон (view);
     *              через этот объект можно добавлять атрибуты для рендеринга страницы.
     * @return имя представления (view), используемое ViewResolver для поиска шаблона
     *         и генерации HTML‑ответа клиенту.
     */
    @GetMapping
    public String home(Model model) {
        return "home";
    }
}